export const getInitialLanguage = (): 'en' | 'zh' => {
    const saved = localStorage.getItem('planner_ui_lang');
    if (saved === 'en' || saved === 'zh') return saved;
    const browserLang = navigator.language.toLowerCase();
    if (browserLang.startsWith('zh')) return 'zh';
    return 'en';
};

export const saveLanguage = (lang: 'en' | 'zh') => {
    localStorage.setItem('planner_ui_lang', lang);
};

export const useTranslation = (lang: 'en' | 'zh') => {
    return { t: (key: keyof typeof translations['en']) => translations[lang][key] || key };
};
